using System;
using System.Collections.Generic;
using System.IO;

namespace DistanceConverter
{
    class Program
    {
        static void Main(string[] args)
        {
           
            while (true)
            {
                var line = Console.ReadLine();
                var count = int.Parse(line);
                if (count < 0)
                {
                    break;
                }
            }
           
        }
        
    }

}
